# Внешний цикл: итерация по числам от 1 до 5
for i in range(1, 6):
    # Внутренний цикл: итерация по числам от 1 до 5
    for j in range(1, 6):
        # Вычисляем произведение текущих значений i и j
        product = i * j
        # Выводим результат умножения в формате "i * j = product"
        print(f"{i} * {j} = {product}")
    # Разделяем блоки таблицы умножения для удобства чтения
    print()  # Пустая строка для отделения каждой строки таблицы
